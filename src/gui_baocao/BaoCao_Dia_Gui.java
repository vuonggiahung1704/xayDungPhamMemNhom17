	public void XoaHetDuLieuTrenTableModel() {
		DefaultTableModel dm = (DefaultTableModel) jTableKhachHang.getModel();
		dm.getDataVector().removeAllElements();
	}

	public void DocDuLieuDatabaseVaoTable(String txt) {
		XoaHetDuLieuTrenTableModel();
		List<TieuDe> list = new ArrayList<TieuDe>();
		qltd = new QuanLyTieuDe();
		if (txt.equals(""))
			list = qltd.danhSachTieuDe();
		else
			list = qltd.tim_TieuDe(txt);

		if (list.size() == 0) {
			JOptionPane.showMessageDialog(null, "Không tìm thấy");
			list = qltd.danhSachTieuDe();
		}

		DefaultTableModel dm = (DefaultTableModel) jTableKhachHang.getModel();
		for (TieuDe td : list) {
			int soluong = qltd.soLuongDia(td.getMaTD());
			int kho = qltd.soLuongDia_CoSan(td.getMaTD());
			int choThue = qltd.soLuongDia_ChoThue(td.getMaTD());
			int duocDat = qltd.soLuongDia_DuocDatHang(td.getMaTD());
			int datHang = qltd.soLuongDia_DatHang(td.getMaTD());
			dm.addRow(new Object[] { td.getMaTD(), td.getTenTD(), soluong, kho, choThue, duocDat, datHang });
		}
	}
	
